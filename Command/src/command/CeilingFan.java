package command;

public class CeilingFan {
	
	public void high() {
		System.out.println("ventilador máxima potência");
	}
	
	public void medium() {
		System.out.println("ventilador média potência");
	}
	
	public void low() {
		System.out.println("ventilador baixa potência");
	}
	
	public void off() {
		System.out.println("desligando ventilador.");
	}
	
	public void on() {
		System.out.println("ligando ventilador.");
	}
	
	public void getSpeet() {
		System.out.println("analisando velocidade atual.");
	}

}
